import { PATCH_MARKER } from "./constants.js";

export function buildHelperSource({ configPath }) {
  const lines = [
    'import { app, protocol, session, webContents } from "electron";',
    'import fs from "node:fs";',
    'import os from "node:os";',
    'import path from "node:path";',
    'import { execFile } from "node:child_process";',
    "",
    `const CONFIG_PATH = ${JSON.stringify(configPath)};`,
    `const PATCH_MARKER = ${JSON.stringify(PATCH_MARKER)};`,
    "",
    "function normalizeModelName(value) {",
    '  if (typeof value !== "string") {',
    '    return "";',
    "  }",
    "  const trimmed = value.trim();",
    "  if (!trimmed) {",
    '    return "";',
    "  }",
    '  const prefixed = trimmed.split("//");',
    "  return prefixed[prefixed.length - 1].trim();",
    "}",
    "",
    "function getCandidateNames(current) {",
    "  const exact = [];",
    "  const prefixed = [];",
    '  for (const key of ["model_name", "config_name", "custom_model_id", "id", "model_id", "name", "model"]) {',
    '    if (typeof current[key] !== "string") {',
    "      continue;",
    "    }",
    "    const value = current[key].trim();",
    "    if (!value) {",
    "      continue;",
    "    }",
    "    if (value.includes(\"//\")) {",
    "      prefixed.push(value);",
    "    } else {",
    "      exact.push(value);",
    "    }",
    "  }",
    "  return { exact, prefixed };",
    "}",
    "",
    "function isCustomModel(current) {",
    "  return current.is_custom_model === 1 || current.is_custom_model === true || current.isCustomModel === true || current.custom_model === true || current.customModel === true || current.model_source === 4 || current.modelSource === 4;",
    "}",
    "",
    "function findOverrideName(current, candidates, models) {",
    "  const exactMatch = candidates.exact.find((item) => Object.prototype.hasOwnProperty.call(models, item));",
    "  if (exactMatch) {",
    "    return exactMatch;",
    "  }",
    "  const normalizedMatch = candidates.prefixed",
    "    .map((item) => normalizeModelName(item))",
    "    .find((item) => item && Object.prototype.hasOwnProperty.call(models, item));",
    "  if (normalizedMatch) {",
    "    return normalizedMatch;",
    "  }",
    "  return undefined;",
    "}",
    "",
    "function applyContextWindow(current, contextWindowTokens) {",
    "  current.context_window_sizes = [contextWindowTokens];",
    "  current.context_window_size = contextWindowTokens;",
    "  current.context_window_tokens = contextWindowTokens;",
    "  current.max_context_tokens = contextWindowTokens;",
    "  current.maxContextTokens = contextWindowTokens;",
    "  current.contextWindowTokens = contextWindowTokens;",
    "  current.contextWindow = contextWindowTokens;",
    "  current.prompt_max_tokens = contextWindowTokens;",
    "  current.promptMaxTokens = contextWindowTokens;",
    "  if (current.toolcall_history_max_tokens == null) {",
    "    current.toolcall_history_max_tokens = contextWindowTokens;",
    "  }",
    "}",
    "",
    "function applyOverridesToObject(target, overrides) {",
    '  if (!target || typeof target !== "object") {',
    "    return target;",
    "  }",
    "  const models = overrides?.models || {};",
    "  const queue = [target];",
    "  const seen = new WeakSet();",
    "  while (queue.length > 0) {",
    "    const current = queue.pop();",
    '    if (!current || typeof current !== "object" || seen.has(current)) {',
    "      continue;",
    "    }",
    "    seen.add(current);",
    "",
    "    const matchedName = findOverrideName(current, getCandidateNames(current), models);",
    "    if (matchedName) {",
    "      const override = models[matchedName];",
    "      const contextWindowTokens = override?.context_window_tokens;",
    "      if (Number.isInteger(contextWindowTokens) && contextWindowTokens > 0) {",
    "        applyContextWindow(current, contextWindowTokens);",
    "      }",
    "    }",
    "",
    "    const children = Array.isArray(current) ? current : Object.values(current);",
    "    for (const child of children) {",
    '      if (child && typeof child === "object") {',
    "        queue.push(child);",
    "      }",
    "    }",
    "  }",
    "  return target;",
    "}",
    "",
    "function buildInjectionSource(overrides) {",
    "  const overridesLiteral = {",
    "    version: overrides.version || 1,",
    "    models: overrides.models || {}",
    "  };",
    "  return `(() => {",
    "    if (globalThis.__TRAE_CONTEXT_PATCHER_INSTALLED__) {",
    "      return;",
    "    }",
    "    globalThis.__TRAE_CONTEXT_PATCHER_INSTALLED__ = true;",
    "    const overrides = ${JSON.stringify(overridesLiteral)};",
    "",
    "    const normalizeModelName = (value) => {",
    '      if (typeof value !== "string") return "";',
    "      const trimmed = value.trim();",
    '      if (!trimmed) return "";',
    '      const prefixed = trimmed.split("//");',
    "      return prefixed[prefixed.length - 1].trim();",
    "    };",
    "",
    "    const getCandidateNames = (current) => {",
    "      const exact = [];",
    "      const prefixed = [];",
    '      for (const key of ["model_name", "config_name", "custom_model_id", "id", "model_id", "name", "model"]) {',
    '        if (typeof current[key] !== "string") continue;',
    "        const value = current[key].trim();",
    "        if (!value) continue;",
    "        if (value.includes(\"//\")) prefixed.push(value);",
    "        else exact.push(value);",
    "      }",
    "      return { exact, prefixed };",
    "    };",
    "",
    "    const isCustomModel = (current) => current.is_custom_model === 1 || current.is_custom_model === true || current.isCustomModel === true || current.custom_model === true || current.customModel === true || current.model_source === 4 || current.modelSource === 4;",
    "",
    "    const findOverrideName = (current, candidates, models) => {",
    "      const exactMatch = candidates.exact.find((item) => Object.prototype.hasOwnProperty.call(models, item));",
    "      if (exactMatch) return exactMatch;",
    "      const normalizedMatch = candidates.prefixed",
    "        .map((item) => normalizeModelName(item))",
    "        .find((item) => item && Object.prototype.hasOwnProperty.call(models, item));",
    "      if (normalizedMatch) return normalizedMatch;",
    "      return undefined;",
    "    };",
    "",
    "    const applyContextWindow = (current, contextWindowTokens) => {",
    "      current.context_window_sizes = [contextWindowTokens];",
    "      current.context_window_size = contextWindowTokens;",
    "      current.context_window_tokens = contextWindowTokens;",
    "      current.max_context_tokens = contextWindowTokens;",
    "      current.maxContextTokens = contextWindowTokens;",
    "      current.contextWindowTokens = contextWindowTokens;",
    "      current.contextWindow = contextWindowTokens;",
    "      current.prompt_max_tokens = contextWindowTokens;",
    "      current.promptMaxTokens = contextWindowTokens;",
    "      if (current.toolcall_history_max_tokens == null) current.toolcall_history_max_tokens = contextWindowTokens;",
    "    };",
    "",
    "    const applyOverridesToObject = (target, localOverrides) => {",
    '      if (!target || typeof target !== "object") return target;',
    "      const models = localOverrides?.models || {};",
    "      const queue = [target];",
    "      const seen = new WeakSet();",
    "      while (queue.length > 0) {",
    "        const current = queue.pop();",
    '        if (!current || typeof current !== "object" || seen.has(current)) continue;',
    "        seen.add(current);",
    "",
    "        const matchedName = findOverrideName(current, getCandidateNames(current), models);",
    "        if (matchedName) {",
    "          const override = models[matchedName];",
    "          const contextWindowTokens = override?.context_window_tokens;",
    "          if (Number.isInteger(contextWindowTokens) && contextWindowTokens > 0) {",
    "            applyContextWindow(current, contextWindowTokens);",
    "          }",
    "        }",
    "",
    "        const children = Array.isArray(current) ? current : Object.values(current);",
    "        for (const child of children) {",
    '          if (child && typeof child === "object") queue.push(child);',
    "        }",
    "      }",
    "      return target;",
    "    };",
    "",
    "    const originalJsonParse = JSON.parse.bind(JSON);",
    "    JSON.parse = (...args) => applyOverridesToObject(originalJsonParse(...args), overrides);",
    "",    '    if (typeof Response !== "undefined" && Response.prototype && typeof Response.prototype.json === "function") {',
    "      const originalResponseJson = Response.prototype.json;",
    "      Response.prototype.json = async function (...args) {",
    "        const result = await originalResponseJson.apply(this, args);",
    "        return applyOverridesToObject(result, overrides);",
    "      };",
    "    }",
    "  })();`;",
    "}",
    "",
    "function getStateDbPath() {",
    "  const appData = process.env.APPDATA || path.join(os.homedir(), 'AppData', 'Roaming');",
    "  return path.join(appData, 'Trae', 'User', 'globalStorage', 'state.vscdb');",
    "}",
    "",
    "function buildStatePatchScript(overrides) {",
    "  const tokenValues = Object.values(overrides?.models || {})",
    "    .map((item) => item?.context_window_tokens)",
    "    .filter((item) => Number.isInteger(item) && item > 0);",
    "  const token = tokenValues[0];",
    "  if (!token) return '';",
    "  return `",
    "import json, os, sqlite3, sys",
    "db = sys.argv[1]",
    "token = int(sys.argv[2])",
    "targets = {'openai//gpt-5.4', 'gpt-5.4'}",
    "def is_target(obj):",
    "    if not isinstance(obj, dict): return False",
    "    name = str(obj.get('name','')).lower()",
    "    display = str(obj.get('display_name','')).lower()",
    "    base = str(obj.get('base_url','')).lower()",
    "    custom = str(obj.get('custom_model_id',''))",
    "    return name in targets or display == 'gpt-5.4' or 'spongyicybulk-clip.hf.space' in base or custom == '330034820'",
    "def patch_model(obj):",
    "    if not is_target(obj): return False",
    "    obj['prompt_max_tokens'] = token",
    "    obj['context_window_size'] = token",
    "    features = obj.setdefault('features', {})",
    "    cw = features.setdefault('context_windows', {})",
    "    cw['enable'] = True",
    "    data = cw.setdefault('data', {})",
    "    data['dev_context'] = token",
    "    data['max_context'] = token",
    "    data['max_context_list'] = [token]",
    "    if data.get('dev_turns') is None: data['dev_turns'] = 200",
    "    if data.get('max_turns') is None: data['max_turns'] = 200",
    "    return True",
    "def walk(x):",
    "    hit = False",
    "    if isinstance(x, dict):",
    "        if patch_model(x): hit = True",
    "        for v in x.values():",
    "            if walk(v): hit = True",
    "    elif isinstance(x, list):",
    "        for v in x:",
    "            if walk(v): hit = True",
    "    return hit",
    "conn = sqlite3.connect(db, timeout=1)",
    "cur = conn.cursor()",
    "rows = cur.execute(\"select key,value from ItemTable where value like '%gpt-5.4%' or value like '%spongyicybulk%'\").fetchall()",
    "changed = 0",
    "for key, val in rows:",
    "    try: data = json.loads(val)",
    "    except Exception: continue",
    "    if walk(data):",
    "        cur.execute('update ItemTable set value=? where key=?', (json.dumps(data, ensure_ascii=False, separators=(',',':')), key))",
    "        changed += 1",
    "conn.commit()",
    "conn.close()",
    "print(changed)",
    "`;",
    "}",
    "",
    "function patchStateDatabase(overrides) {",
    "  const dbPath = getStateDbPath();",
    "  if (!fs.existsSync(dbPath)) return;",
    "  const script = buildStatePatchScript(overrides);",
    "  if (!script) return;",
    "  execFile('python', ['-c', script, dbPath, String(Object.values(overrides.models || {})[0]?.context_window_tokens || 0)], { windowsHide: true, timeout: 5000 }, (error) => {",
    "    if (error) console.warn(PATCH_MARKER, 'state database patch failed', error.message);",
    "  });",
    "}",
    "",
    "function startStateDatabasePatcher(overrides) {",
    "  patchStateDatabase(overrides);",
    "  setInterval(() => patchStateDatabase(overrides), 3000);",
    "}",
    "",
    "function installPatch(window) {",
    "  try {",
    "    if (!fs.existsSync(CONFIG_PATH)) {",
    "      return;",
    "    }",
    '    const raw = fs.readFileSync(CONFIG_PATH, "utf8");',
    "    const overrides = JSON.parse(raw);",
    "    startStateDatabasePatcher(overrides);",
    "    installNetworkPatcher(overrides);",
    "    window.webContents",
    "      .executeJavaScript(buildInjectionSource(overrides))",
    '      .catch((error) => console.warn(PATCH_MARKER, "renderer injection failed", error));',
    "  } catch (error) {",
    '    console.warn(PATCH_MARKER, "failed to load override file", CONFIG_PATH, error);',
    "  }",
    "}",
    "",
    "function patchTextPayload(text, overrides) {",
    "  const token = Object.values(overrides?.models || {})[0]?.context_window_tokens;",
    "  if (!Number.isInteger(token) || token <= 0 || typeof text !== 'string') return text;",
    "  const display = token % 1024 === 0 ? `${token / 1024}K` : String(token);",
    "  return text",
    "    .replace(/128K/g, display)",
    "    .replace(/131072/g, String(token))",
    "    .replace(/\"dev_context\":null/g, `\"dev_context\":${token}`)",
    "    .replace(/\"max_context\":null/g, `\"max_context\":${token}`)",
    "    .replace(/\"max_context_list\":null/g, `\"max_context_list\":[${token}]`);",
    "}",
    "",
    "function installNetworkPatcher(overrides) {",
    "  try {",
    "    const filter = { urls: ['https://*/*', 'http://*/*'] };",
    "    session.defaultSession.webRequest.onBeforeRequest(filter, (details, callback) => {",
    "      callback({});",
    "    });",
    "    const originalFetch = globalThis.fetch;",
    "    if (typeof originalFetch === 'function' && !globalThis.__TRAE_CONTEXT_PATCHER_FETCH__) {",
    "      globalThis.__TRAE_CONTEXT_PATCHER_FETCH__ = true;",
    "      globalThis.fetch = async (...args) => {",
    "        const response = await originalFetch(...args);",
    "        return response;",
    "      };",
    "    }",
    "  } catch (error) {",
    "    console.warn(PATCH_MARKER, 'network patch install failed', error);",
    "  }",
    "}",
    "",
    "function installPatchForWebContents(contents) {",
    "  if (!contents || contents.isDestroyed()) return;",
    "  const run = () => installPatch({ webContents: contents });",
    "  contents.on('dom-ready', run);",
    "  contents.on('did-finish-load', run);",
    "  run();",
    "}",
    "",
    'app.on("browser-window-created", (_event, window) => {',
    '  installPatchForWebContents(window.webContents);',
    "});",
    "",
    "app.whenReady().then(() => {",
    "  for (const contents of webContents.getAllWebContents()) {",
    "    installPatchForWebContents(contents);",
    "  }",
    "  webContents.on('created', (_event, contents) => installPatchForWebContents(contents));",
    "});",
    "",
  ];

  return lines.join("\n");
}
