    "    hit = False",
    "    if isinstance(x, dict):",
    "        if patch_model(x): hit = True",
    "        for v in x.values():",
    "            if walk(v): hit = True",
    "    elif isinstance(x, list):",
    "        for v in x:",
    "            if walk(v): hit = True",
    "    return hit",
    "conn = sqlite3.connect(db, timeout=1)",
    "cur = conn.cursor()",
    "rows = cur.execute(\"select key,value from ItemTable\").fetchall()",
    "changed = 0",
    "for key, val in rows:",
    "    try: data = json.loads(val)",
