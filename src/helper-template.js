    "    data = cw.setdefault('data', {})",
    "    data['dev_context'] = token",
    "    data['max_context'] = token",
    "    data['max_context_list'] = [token]",
    "    if data.get('dev_turns') is None: data['dev_turns'] = 200",
    "    if data.get('max_turns') is None: data['max_turns'] = 200",
