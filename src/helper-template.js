    "",
    "function buildStatePatchTargets(overrides) {",
    "  const targets = {};",
    "  for (const [modelId, override] of Object.entries(overrides?.models || {})) {",
    "    const token = override?.context_window_tokens;",
    "    if (!Number.isInteger(token) || token <= 0) continue;",
    "    const normalized = normalizeModelName(modelId).toLowerCase();",
    "    if (!normalized) continue;",
    "    targets[normalized] = token;",
    "    const bare = normalized.split('//').pop();",
    "    if (bare) {",
    "      targets[bare] = token;",
    "      targets[`openai//${bare}`] = token;",
    "    }",
    "  }",
    "  return targets;",
    "}",
    "",
    "function buildStatePatchScript(overrides) {",
    "  const targets = buildStatePatchTargets(overrides);",
    "  if (Object.keys(targets).length === 0) return '';",
    "  return `",
    "import json, os, sqlite3, sys",
    "db = sys.argv[1]",
    "targets = {str(k).lower(): int(v) for k, v in json.loads(sys.argv[2]).items()}",
    "legacy_token = targets.get('gpt-5.4') or targets.get('openai//gpt-5.4')",
    "def norm(value):",
    "    return str(value or '').strip().lower()",
    "def bare(value):",
    "    value = norm(value)",
    "    return value.split('//')[-1] if value else ''",
    "def configured_token(value):",
    "    value = norm(value)",
    "    if value in targets: return targets[value]",
    "    stripped = bare(value)",
    "    if stripped in targets: return targets[stripped]",
    "    prefixed = 'openai//' + stripped if stripped else ''",
    "    if prefixed in targets: return targets[prefixed]",
    "    return None",
    "def target_token(obj):",
    "    if not isinstance(obj, dict): return None",
    "    for key in ('name', 'display_name', 'model', 'model_id', 'id', 'config_name', 'custom_model_id'):",
    "        token = configured_token(obj.get(key))",
    "        if token: return token",
    "    base = norm(obj.get('base_url',''))",
    "    custom = norm(obj.get('custom_model_id',''))",
    "    if legacy_token and ('spongyicybulk-clip.hf.space' in base or custom == '330034820'): return legacy_token",
    "    return None",
    "SOLO_TOOL_PROTOCOL_CONFIG = {",
    "    'apply_file_path': True,",
    "    'enable_invalid_json_hint': True,",
    "    'is_new_pe': True,",
    "    'native_function_call': True,",
    "    'native_keep_finish_tool': False,",
    "    'parallel_tool_calling': False,",
    "    'use_v2_process': True,",
