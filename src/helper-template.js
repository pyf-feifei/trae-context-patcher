    "conn.commit()",
    "conn.close()",
    "print(changed)",
    "`;",
    "}",
    "",
    "function patchStateDatabase(overrides) {",
    "  const dbPath = getStateDbPath();",
    "  if (!fs.existsSync(dbPath)) return;",
    "  const script = buildStatePatchScript(overrides);",
    "  if (!script) return;",
    "  execFile('python', ['-c', script, dbPath, JSON.stringify(buildStatePatchTargets(overrides))], { windowsHide: true, timeout: 5000 }, (error) => {",
