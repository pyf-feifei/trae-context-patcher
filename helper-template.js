import { PATCH_MARKER } from "./constants.js";

export function buildHelperSource({ configPath }) {
  const lines = [
    'import { app } from "electron";',
    'import fs from "node:fs";',
    "",
    `const CONFIG_PATH = ${JSON.stringify(configPath)};`,
    `const PATCH_MARKER = ${JSON.stringify(PATCH_MARKER)};`,
    "",
    "function normalizeModelName(value) {",
    '  if (typeof value !== "string") {',
    '    return "";',
    "  }",
    "  const trimmed = value.trim();",
    "  if (!trimmed) {",
    '    return "";',
    "  }",
    '  const prefixed = trimmed.split("//");',
    "  return prefixed[prefixed.length - 1].trim();",
    "}",
    "",
    "function getCandidateNames(current) {",
    "  const exact = [];",
    "  const prefixed = [];",
    '  for (const key of ["model_name", "config_name", "custom_model_id", "id", "model_id", "name", "model"]) {',
    '    if (typeof current[key] !== "string") {',
    "      continue;",
    "    }",
    "    const value = current[key].trim();",
    "    if (!value) {",
    "      continue;",
    "    }",
    "    if (value.includes(\"//\")) {",
    "      prefixed.push(value);",
    "    } else {",
    "      exact.push(value);",
    "    }",
    "  }",
    "  return { exact, prefixed };",
    "}",
    "",
    "function isCustomModel(current) {",
    "  return current.is_custom_model === 1 || current.is_custom_model === true || current.isCustomModel === true || current.custom_model === true || current.customModel === true || current.model_source === 4 || current.modelSource === 4;",
    "}",
    "",
    "function findOverrideName(current, candidates, models) {",
    "  const exactMatch = candidates.exact.find((item) => Object.prototype.hasOwnProperty.call(models, item));",
    "  if (exactMatch) {",
    "    return exactMatch;",
    "  }",
    "  if (isCustomModel(current)) {",
    "    const normalizedMatch = candidates.prefixed",
    "      .map((item) => normalizeModelName(item))",
    "      .find((item) => item && Object.prototype.hasOwnProperty.call(models, item));",
    "    if (normalizedMatch) {",
    "      return normalizedMatch;",
    "    }",
    "  }",
    "  return undefined;",
    "}",
    "",
    "function applyContextWindow(current, contextWindowTokens) {",
    "  current.context_window_sizes = [contextWindowTokens];",
    "  current.context_window_size = contextWindowTokens;",
    "  current.context_window_tokens = contextWindowTokens;",
    "  current.max_context_tokens = contextWindowTokens;",
    "  current.maxContextTokens = contextWindowTokens;",
    "  current.contextWindowTokens = contextWindowTokens;",
    "  current.contextWindow = contextWindowTokens;",
    "  current.prompt_max_tokens = contextWindowTokens;",
    "  current.promptMaxTokens = contextWindowTokens;",
    "  if (current.toolcall_history_max_tokens == null) {",
    "    current.toolcall_history_max_tokens = contextWindowTokens;",
    "  }",
    "}",
    "",
    "function applyOverridesToObject(target, overrides) {",
    '  if (!target || typeof target !== "object") {',
    "    return target;",
    "  }",
    "  const models = overrides?.models || {};",
    "  const queue = [target];",
    "  const seen = new WeakSet();",
    "  while (queue.length > 0) {",
    "    const current = queue.pop();",
    '    if (!current || typeof current !== "object" || seen.has(current)) {',
    "      continue;",
    "    }",
    "    seen.add(current);",
    "",
    "    const matchedName = findOverrideName(current, getCandidateNames(current), models);",
    "    if (matchedName) {",
    "      const override = models[matchedName];",
    "      const contextWindowTokens = override?.context_window_tokens;",
    "      if (Number.isInteger(contextWindowTokens) && contextWindowTokens > 0) {",
    "        applyContextWindow(current, contextWindowTokens);",
    "      }",
    "    }",
    "",
    "    const children = Array.isArray(current) ? current : Object.values(current);",
    "    for (const child of children) {",
    '      if (child && typeof child === "object") {',
    "        queue.push(child);",
    "      }",
    "    }",
    "  }",
    "  return target;",
    "}",
    "",
    "function buildInjectionSource(overrides) {",
    "  const overridesLiteral = {",
    "    version: overrides.version || 1,",
    "    models: overrides.models || {}",
    "  };",
    "  return `(() => {",
    "    if (globalThis.__TRAE_CONTEXT_PATCHER_INSTALLED__) {",
    "      return;",
    "    }",
    "    globalThis.__TRAE_CONTEXT_PATCHER_INSTALLED__ = true;",
    "    const overrides = ${JSON.stringify(overridesLiteral)};",
    "",
    "    const normalizeModelName = (value) => {",
    '      if (typeof value !== "string") return "";',
    "      const trimmed = value.trim();",
    '      if (!trimmed) return "";',
    '      const prefixed = trimmed.split("//");',
    "      return prefixed[prefixed.length - 1].trim();",
    "    };",
    "",
    "    const getCandidateNames = (current) => {",
    "      const exact = [];",
    "      const prefixed = [];",
    '      for (const key of ["model_name", "config_name", "custom_model_id", "id", "model_id", "name", "model"]) {',
    '        if (typeof current[key] !== "string") continue;',
    "        const value = current[key].trim();",
    "        if (!value) continue;",
    "        if (value.includes(\"//\")) prefixed.push(value);",
    "        else exact.push(value);",
    "      }",
    "      return { exact, prefixed };",
    "    };",
    "",
    "    const isCustomModel = (current) => current.is_custom_model === 1 || current.is_custom_model === true || current.isCustomModel === true || current.custom_model === true || current.customModel === true || current.model_source === 4 || current.modelSource === 4;",
    "",
    "    const findOverrideName = (current, candidates, models) => {",
    "      const exactMatch = candidates.exact.find((item) => Object.prototype.hasOwnProperty.call(models, item));",
    "      if (exactMatch) return exactMatch;",
    "      if (isCustomModel(current)) {",
    "        const normalizedMatch = candidates.prefixed",
    "          .map((item) => normalizeModelName(item))",
    "          .find((item) => item && Object.prototype.hasOwnProperty.call(models, item));",
    "        if (normalizedMatch) return normalizedMatch;",
    "      }",
    "      return undefined;",
    "    };",
    "",
    "    const applyContextWindow = (current, contextWindowTokens) => {",
    "      current.context_window_sizes = [contextWindowTokens];",
    "      current.context_window_size = contextWindowTokens;",
    "      current.context_window_tokens = contextWindowTokens;",
    "      current.max_context_tokens = contextWindowTokens;",
    "      current.maxContextTokens = contextWindowTokens;",
    "      current.contextWindowTokens = contextWindowTokens;",
    "      current.contextWindow = contextWindowTokens;",
    "      current.prompt_max_tokens = contextWindowTokens;",
    "      current.promptMaxTokens = contextWindowTokens;",
    "      if (current.toolcall_history_max_tokens == null) current.toolcall_history_max_tokens = contextWindowTokens;",
    "    };",
    "",
    "    const applyOverridesToObject = (target, localOverrides) => {",
    '      if (!target || typeof target !== "object") return target;',
    "      const models = localOverrides?.models || {};",
    "      const queue = [target];",
    "      const seen = new WeakSet();",
    "      while (queue.length > 0) {",
    "        const current = queue.pop();",
    '        if (!current || typeof current !== "object" || seen.has(current)) continue;',
    "        seen.add(current);",
    "",
    "        const matchedName = findOverrideName(current, getCandidateNames(current), models);",
    "        if (matchedName) {",
    "          const override = models[matchedName];",
    "          const contextWindowTokens = override?.context_window_tokens;",
    "          if (Number.isInteger(contextWindowTokens) && contextWindowTokens > 0) {",
    "            applyContextWindow(current, contextWindowTokens);",
    "          }",
    "        }",
    "",
    "        const children = Array.isArray(current) ? current : Object.values(current);",
    "        for (const child of children) {",
    '          if (child && typeof child === "object") queue.push(child);',
    "        }",
    "      }",
    "      return target;",
    "    };",
    "",
    "    const originalJsonParse = JSON.parse.bind(JSON);",
    "    JSON.parse = (...args) => applyOverridesToObject(originalJsonParse(...args), overrides);",
    "",
    '    if (typeof Response !== "undefined" && Response.prototype && typeof Response.prototype.json === "function") {',
    "      const originalResponseJson = Response.prototype.json;",
    "      Response.prototype.json = async function (...args) {",
    "        const result = await originalResponseJson.apply(this, args);",
    "        return applyOverridesToObject(result, overrides);",
    "      };",
    "    }",
    "  })();`;",
    "}",
    "",
    "function installPatch(window) {",
    "  try {",
    "    if (!fs.existsSync(CONFIG_PATH)) {",
    "      return;",
    "    }",
    '    const raw = fs.readFileSync(CONFIG_PATH, "utf8");',
    "    const overrides = JSON.parse(raw);",
    "    window.webContents",
    "      .executeJavaScript(buildInjectionSource(overrides))",
    '      .catch((error) => console.warn(PATCH_MARKER, "renderer injection failed", error));',
    "  } catch (error) {",
    '    console.warn(PATCH_MARKER, "failed to load override file", CONFIG_PATH, error);',
    "  }",
    "}",
    "",
    'app.on("browser-window-created", (_event, window) => {',
    '  window.webContents.on("did-finish-load", () => installPatch(window));',
    "});",
    "",
  ];

  return lines.join("\n");
}
